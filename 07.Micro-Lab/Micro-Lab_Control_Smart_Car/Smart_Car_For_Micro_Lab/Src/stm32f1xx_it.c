/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */

  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
	if(__HAL_UART_GET_IT_SOURCE(&huart2,UART_IT_IDLE)!=RESET)
  {
		esp8266_rxdata=(USART2->DR & (uint16_t)0x01FF ); 
    strEsp8266_Fram_Record .InfBit .FramFinishFlag = 1;       
    ucTcpClosedFlag = strstr(strEsp8266_Fram_Record .Data_RX_BUF, "CLOSED\r\n" ) ? 1 : 0;
  }
  /* USER CODE END USART2_IRQn 1 */
}

/* USER CODE BEGIN 1 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *UartHandle)
{
		//预留1个字节写结束符
    if(strEsp8266_Fram_Record.InfBit.FramLength < (RX_BUF_MAX_LEN - 1))                   
        strEsp8266_Fram_Record.Data_RX_BUF[strEsp8266_Fram_Record.InfBit.FramLength++] = esp8266_rxdata;

    HAL_UART_Receive_IT(&huart2, &esp8266_rxdata, 1);
}
void HAL_SYSTICK_Callback(void)
{
    static uint8_t step = 0 ;

    //如果脉冲数不为0
    if(Motor_Pulse)
    {
        MOTOR_CONTROLD(step, direction);
        step++ ;

        if(8 == step)
            step = 0 ;

        Motor_Pulse-- ;
    }
    else
    {
        //关闭电机
        CLOSE_MOTOR();
    }
}
